public class dwaughMod1 {
    public static void main(String[] args) {
        System.out.println("Let's solve this equation:");
        System.out.println("4(1 - (1/3) + (1/5) - (1/7) + (1/9) - (1/11) + (1/13))= ");
        System.out.print("4(1-1/3+1/5-1/7+1/9-1/11+1/13)= ");
        System.out.println((4*(1-(1/3)+(1/5)-(1/7)+(1/9)-(1/11)+(1/13))));
        System.out.println("Now with decimals");
        System.out.println("4(1.0-1.0/3.0+1.0/5.0-1.0/7.0+1.0/9.0-1.0/11.0+1.0/13.0)");
        System.out.print("4(1.0-1.0/3.0+1.0/5.0-1.0/7.0+1.0/9.0-1.0/11.0+1.0/13.0)= ");
        System.out.println((4*(1.0-(1.0/3.0)+(1.0/5.0)-(1.0/7.0)+(1.0/9.0)-(1.0/11.0)+(1.0/13.))));
        System.out.println("Two different answers!");


    }
}

// It would seem that using decimals gives a more accurate number where as whole numbers will round to nearest whole number.


